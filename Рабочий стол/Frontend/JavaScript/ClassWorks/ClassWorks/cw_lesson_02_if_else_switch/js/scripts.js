/*Иногда, в зависимости от условия, нужно выполнить различные действия. Для этого используется оператор if.*/

/*Функция prompt принимает два аргумента: title, default. Она выводит модальное окно с заголовком title, полем для ввода текста, заполненным строкой по умолчанию default и кнопками OK/CANCEL.*/
var year = prompt('В каком году появилась спецификация ECMA-262 5.1?', '');

/*Оператор if («если») получает условие, в примере выше это year != 2011. Он вычисляет его, и если результат – true, то выполняет команду.*/

if (year != 2011) alert( 'А вот и неправильно!' );

/*Если нужно выполнить более одной команды – они оформляются блоком кода в фигурных скобках. Рекомендуется использовать фигурные скобки всегда, даже когда команда одна. Это улучшает читаемость кода.*/

if (year == 2011) {
	alert( 'Да, это...' );
	alert( '... правильный ответ!' );
}

