/*Конструкция switch заменяет собой сразу несколько if. Она представляет собой более наглядный способ сравнить выражение сразу с несколькими вариантами.

switch(x) {
  case 'value1':  // if (x === 'value1')
    ...
    [break]
  case 'value2':  // if (x === 'value2')
    ...
    [break]
  default:
    ...
    [break]
}

Переменная x проверяется на строгое равенство первому значению value1, затем второму value2 и так далее. Если соответствие установлено – switch начинает выполняться от соответствующей директивы case и далее, до ближайшего break (или до конца switch). Если ни один case не совпал – выполняется (если есть) вариант default.*/

/*Здесь оператор switch последовательно сравнит a со всеми вариантами из case. Сначала 3, затем – так как нет совпадения – 4. Совпадение найдено, будет выполнен этот вариант, со строки alert('В точку!') и далее, до ближайшего break, который прервёт выполнение.*/

var a = 2 + 2;
switch (a) {
	case 3:
		alert('Маловато');
	break;
	case 4:
		alert('В точку!');
	break;
	case 5:
		alert('Перебор');
	break;
	default:
		alert('Я таких значений не знаю');
}

/*Если break нет, то выполнение пойдёт ниже по следующим case, при этом остальные проверки игнорируются. В примере последовательно выполнятся три alert:
	alert( 'В точку!' );
	alert( 'Перебор' );
	alert( 'Я таких значений не знаю' );*/

var a = 3 + 2;
switch (a) {
	case 4:
		alert( 'Маловато' );
	case 5:
		alert( 'В точку!' );
	case 6:
		alert( 'Перебор' );
	default:
		alert( 'Я таких значений не знаю' );
}

/*В case могут быть любые выражения, в том числе включающие в себя переменные и функции.*/

